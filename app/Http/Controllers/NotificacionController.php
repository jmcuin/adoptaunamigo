     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        if(!Notificacion::where('email', 'like', $request -> email)->first()){
            $notificacion = new Notificacion;
            $notificacion -> email = $request -> email;
            $notificacion -> save();

            event(new NuevaNotificacion($notificacion));

            return redirect() -> back()-> with('info','¡¡Gracias por registrarte!! Te mantendremos informado(a) de todas nuestras novedades.');
        }else{
            return redirect() -> back();
        }
    }

